export const getRandomWords = (count, excludeWords = []) => {
  const available = vocabulary.filter(w => !excludeWords.includes(w.hanzi));
  const shuffled = [...available].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

export const generateChoices = (correct, allWords) => {
  const distractors = allWords
    .filter(w => w.hanzi !== correct.hanzi)
    .map(w => ({ ...w, score: scoreDistractor(correct, w) }))
    .sort((a, b) => b.score - a.score)
    .slice(0, 12);

  const picked = [];
  const used = new Set();
  for (const d of distractors) {
    if (picked.length >= 3) break;
    if (!used.has(d.hanzi)) {
      picked.push(d);
      used.add(d.hanzi);
    }
  }

  while (picked.length < 3) {
    const rand = allWords[Math.floor(Math.random() * allWords.length)];
    if (rand.hanzi !== correct.hanzi && !used.has(rand.hanzi)) {
      picked.push(rand);
      used.add(rand.hanzi);
    }
  }

  return [...picked, correct].sort(() => Math.random() - 0.5);
};

const scoreDistractor = (correct, candidate) => {
  let score = 0;
  if (correct.pinyin === candidate.pinyin) score += 4;
  else if (correct.pinyin.split('')[0] === candidate.pinyin.split('')[0]) score += 2;
  if (correct.radical === candidate.radical) score += 3;
  if (correct.level === candidate.level) score += 1;
  return score;
};
